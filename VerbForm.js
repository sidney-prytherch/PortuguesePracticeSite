/**
 * 
 * @param {string} infinitive 
 * @param {string} stem 
 * @returns string
 */
function getEIStem(infinitive, stem) {
    if (infinitive.length > 2) {
        switch (infinitive.slice(-3)) {
            case "car": return `${infinitive.slice(0, -3)}qu`
            case "çar": return `${infinitive.slice(0, -3)}c`
            case "gar": return `${infinitive.slice(0, -3)}gu`
        }
    }
    return stem;
}

/**
* @param {string} infinitive 
* @param {string} stem 
* @returns string
*/
function getAOStem(infinitive, stem) {
    if (infinitive.length > 2) {
        switch (infinitive.slice(-3)) {
            case "cer":
            case "cir":
                return `${infinitive.slice(0, -3)}ç`
            case "ger":
            case "gir":
                return `${infinitive.slice(0, -3)}j`
            default:
                let verbEnd = infinitive.slice(-4)
                return (verbEnd === "guir" || verbEnd === "guer") ? infinitive.slice(0, -3) : stem
        }
    }
    return stem
}

const getParticipleAndGerund = (infinitive) => {
    let gerund = `${infinitive.slice(0, -1)}ndo`
    let participle = infinitive.endsWith("ar") ?
        `${infinitive.slice(0, -1)}do` :
        `${infinitive.slice(0, -2)}ido`;
    return { participle, gerund };
}

